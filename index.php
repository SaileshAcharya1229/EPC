      <script>
        (function() {
          const slides = document.getElementById('testimonialSlides');
          const items = slides.querySelectorAll('.testimonial-item');
          const totalSlides = items.length;
          let current = 0;
          let interval = null;

          function showSlide(idx) {
            slides.style.transform = 'translateX(' + (-idx * 100) + '%)';
          }

          function nextSlide() {
            current = (current + 1) % totalSlides;
            showSlide(current);
          }

          function prevSlide() {
            current = (current - 1 + totalSlides) % totalSlides;
            showSlide(current);
          }

          function startAutoSlide() {
            interval = setInterval(nextSlide, 4000);
          }

          function stopAutoSlide() {
            clearInterval(interval);
          }

          document.getElementById('testimonialNext').onclick = function() {
            stopAutoSlide();
            nextSlide();
            startAutoSlide();
          };
          document.getElementById('testimonialPrev').onclick = function() {
            stopAutoSlide();
            prevSlide();
            startAutoSlide();
          };

          // Pause on hover
          slides.parentElement.onmouseenter = stopAutoSlide;
          slides.parentElement.onmouseleave = startAutoSlide;

          // Responsive: always 1 per slide
          function handleResize() {
            slides.style.flexDirection = 'row';
            items.forEach(item => {
              item.style.minWidth = '100%';
            });
          }
          window.addEventListener('resize', handleResize);
          handleResize();

          // Initialize
          showSlide(current);
          startAutoSlide();
        })();
      </script>
